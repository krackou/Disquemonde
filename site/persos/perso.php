<tr><td>
	<p class="p1">Bienvenue dans la Galerie des Personnages du Disque-Monde. 
	C'est un recueil d'informations concernant les personnages que 
	l'on retrouve dans les romans de Pratchett et qui inclue des 
	descriptions, des dessins et la liste des romans dans lesquels 
	ils apparaissent (pas pour tous).<br /></p>

	<p class="p">En effet, que serait un monde, qui se ballade à travers l'espace 
	sur le dos d'une tortue gigantesque, sans une pléyade de personnages 
	tous plus loufoques (voir complètement cinglés) les uns que les 
	autres: très banal me direz-vous ? Je me propose donc de vous 
	présenter ceux que l'on retrouve le plus souvent dans les 
	délires de Pratchett.</p>

	<p class="p">Une grande partie des ces personnages vivent à Ankh-Morpork 
	qui aurait presque pu faire parti de la liste (mais bon... ça collait 
	pas bien avec la présentation que je voulait faire; y m'fallait 
	un chiffre pair alors...).</p>
