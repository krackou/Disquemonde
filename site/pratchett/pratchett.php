    <td colspan="2" height="100%"> <img src="images/photo6.jpg" align="right" width=268 height=219 /> 
      <p class="p1">L'anglais Terry Pratchett est renommé pour son humour léger et son 
		sens aigu de la satyre. Il est connu pour les Annales du Disque-Monde.</p>
      <img src="images/terryi.jpg" width="315" height="307" align="left" />
      <p class="p">C'est durant le mois d'avril 1948, le 28 au matin pour être exact, 
		dans le petit village de Forty Green, Buckinghamshire, que vient au monde 
		le petit Terry David John Pratchett. Né de parents de condition modeste, 
		il découvre la vie dans une Angleterre qui se remet de la guerre. Elève 
		moyen, lecteur peu enthousiaste, il découvre le plaisir des livres avec 
		"le vent dans les saules" de Kenneth Grahame, à 10 ans. 3 ans plus tard, 
		en cours d'anglais, il obtient le premier prix pour une histoire 
		fantastique : Une affaire d'enfer, qui compte les malheurs du diable, 
		obligé de faire de la publicité et de proposer des voyages organisés pour 
		que les bonnes âmes se ruent aux enfers. L'histoire plaît, elle est vendue 
		à un magazine de science-fiction pour 14 £. Il s'achète une machine à 
		écrire.</p>
	
		<p class="p">A 17 ans, il écrit son premier roman : <a href="../biblio/biblio_base.php?book=_others&lang=fr_fr">Le 
	 	Peuple du Tapis (The carpet people)</a>, qu'il publie à 20 ans. Il commence 
  		alors une carrière dans le journalisme provincial qui durera 15 ans. Pourquoi 
  		le journalisme ? "C'est un job d'intérieur sans trucs lourds à porter !".</p>
		<p class="p">Par la suite il travaille comme attaché de presse pour quatre 
		centrales nucléaires : "Radioactivité... Quelle radioactivité? Oh, cette 
		radioactivité.".</p>
	
		
      <p class="p">Il continue d'écrire ; en 1976 : <a href="../biblio/biblio_base.php?book=_others&lang=fr_fr">La 
        Face Obscure du Soleil (The Dark Side of The Sun)</a>, et en 1981, <a href="../biblio/biblio_base.php?book=_others&lang=fr_fr">Strates 
        à Gemmes (Stata)</a>, deux romans de <img src="images/photo5.jpg" width=384 height=260 align="right" alt="Terry Pratchett, Ian Stewart et Jack Cohen" />science-fiction. 
        En 1983, c'est la sortie de <a href="../biblio/biblio_base.php?book=the_colour_of_magic&lang=fr_fr">La Huitième 
        Couleur</a>, l'avènement du Disque-Monde. En 1987, il arrête de travailler 
        pour se consacrer à l'écriture. Il écrit 2 livres par an, il a vendu plus 
        de 7 000 000 d'exemplaires en Angleterre. En quelques mois il atteint 
        le Top 10. Il est traduit en 18 langues et est connu dans plus de 25 pays. 
        La France commence à le découvrir.</p>
      <p class="p">Terry Pratchett a rejoint son personnage fétiche le 12 Mars 2015 à 66 ans «à son domicile, son chat endormi sur son lit et entouré par sa famille» (d'après son éditeur).<br /><br /><br /></p>
